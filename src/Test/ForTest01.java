package Test;

public class ForTest01 {

    public static void main(String[] args) {

        //输出1-5的数字
        for(int i=1; i<=5; i++) {
            System.out.println(i);
        }

        System.out.println("————————————————");

        //输出5-1的数字
        for(int i=5; i>=1; i--) {
            System.out.println(i);
        }

    }

}
