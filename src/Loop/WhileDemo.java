package Loop;

public class WhileDemo {

    public static void main(String[] args) {

        for(int i=1; i<=5; i++){
            System.out.println("HelloWorld");
        }
        System.out.println("----------");

        int j=0;
        while(j<=5){
            System.out.println("HelloWorld");
            j++;
        }
    }

}
