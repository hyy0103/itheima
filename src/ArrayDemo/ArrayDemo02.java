package ArrayDemo;

public class ArrayDemo02 {

    //静态初始化

    public static void main(String[] args) {

        //定义数组
        int[] arr = {1,2,3};

        //输出数组名
        System.out.println(arr);

        //输出数组的元素
        System.out.println(arr[0]);
        System.out.println(arr[1]);
        System.out.println(arr[2]);

    }

}
